-- Exercise 1
-- Write a SQL query to select all the salespeople. Return salesman_id, name, city,
-- commission with the percent sign (%).

SELECT 
  [salesman_id],
  [name],
  [city],
  [comission]*100 AS [comission],
  '%' AS [sign]
FROM
  [dbo].[salesman]

-- Exercise 2
-- Write a SQL query to find the number of orders booked for each day. Return
-- the result in a format like "For 2001-10-10 there are 15 orders".".

SELECT
  'For',
  [ord_date],
  ', there are',
  COUNT(*) AS [count],
  'orders.'
FROM
  [dbo].[orders]
GROUP BY
  [ord_date]

