-- Exercise 6
-- Write a SQL query to find all the customers. Sort the result-set by customer_id.
-- Return cust_name, city, grade.

SELECT
  [cust_name],
  [city],
  [grade]
FROM
  [dbo].[customer]
ORDER BY
  [customer_id]

-- Exercise 7
-- Write a SQL query that calculates the maximum purchase amount generated by each sales
-- person for each order date. Sort the result-set by salesperson id and order date in
-- ascending order. Return salesperson id, order date and maximum purchase amount.

SELECT
  [salesman_id],
  [ord_date],
  MAX(purch_amt) AS [max]
FROM
  [dbo].[orders]
GROUP BY
  [salesman_id],
  [ord_date]
ORDER BY
  [salesman_id],
  [ord_date]

-- Exercise 8
-- Write a SQL query to find all the customers. Sort the result-set in descending
-- order on 3rd field. Return customer name, city and grade.

SELECT
  [cust_name],
  [city],
  [grade]
FROM
  [dbo].[customer]
ORDER BY
  3 DESC  -- 3rd field in SELECT [grade]

-- Exercise 9
-- Write a SQL query that counts the unique orders and the highest purchase amount for
-- each customer. Sort the resultset in descending order on 2nd field. Return customer
-- ID, number of distinct orders and highest purchase amount by each customer.

SELECT
  [customer_id],
  COUNT(*),
  MAX(purch_amt)
FROM
  [dbo].[orders]
GROUP BY
  [customer_id]
ORDER BY

-- Exercise 10
-- Write a SQL query to calculate the summation of purchase amount, total commission
-- (15% for all salespeople) by each order date. Sort the result-set on order date.
-- Return order date, summation of purchase amount and commission.

SELECT
  [ord_date],
  SUM(purch_amt) AS [sum],
  SUM([purch_amt])*0.15 AS [comission]
FROM
  [dbo].[orders]
GROUP BY
  [ord_date]
ORDER BY
  [ord_date]